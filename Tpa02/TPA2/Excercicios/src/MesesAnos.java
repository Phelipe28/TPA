
public class MesesAnos {

	public static void main(String[] args) {
	    //declaração de variáveis 
		int dia,mes,ano;
		
		//atribuições de valores
		dia = 30;
		ano= 365;
		//processamento 
		mes = dia/dia;
	    ano = ano/dia;
		
		//exibição de valores
		
		System.out.println("mes = " +mes);
		
		System.out.println(dia+ " / "+dia+" = "+mes);
		
		
		System.out.println("ano = " +ano);
		
		System.out.println(ano+ " / "+mes+" = "+ano);
		
		
		
		}//fim do método main
	}//fim da classe

